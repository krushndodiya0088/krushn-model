#include <stdio.h>
void main()
{
		int a;
		int b;
		printf("Enter the Value of A: ");
		scanf("%d",&a);
		printf("Enter the Value of B: ");
		scanf("%d",&b);
		printf("Addition : %d\n",(a+b));
		printf("Subtraction : %d\n",(a-b));
		printf("Multiplication : %d\n",(a*b));
		printf("Division : %f\n",((float)a/b));
}